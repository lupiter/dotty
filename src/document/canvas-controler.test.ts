const arrayOf = (
  color: Color,
  width: number,
  height: number
): Uint8ClampedArray => {
  return new Uint8ClampedArray(
    Array(width * height * 4).fill([color.r, color.g, color.b, color.a])
  );
};

const join = (
  a: Uint8ClampedArray,
  b: Uint8ClampedArray
): Uint8ClampedArray => {
  return new Uint8ClampedArray([...a, ...b]);
};

